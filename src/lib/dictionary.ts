// ─── Scoring ───
export function getWordScore(word: string): number {
  const len = word.length;
  if (len <= 4) return 10;
  if (len === 5) return 25;
  if (len === 6) return 60;
  if (len === 7) return 120;
  return 200 + (len - 7) * 80;
}

